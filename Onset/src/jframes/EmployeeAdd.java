    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "insert into CONTACT values(?,?,?,?,?,?,?)";
            PreparedStatement ptst = db.con.prepareStatement(sql);
            ptst.setString(1, fn.getText());
            ptst.setString(2, ln.getText());
            ptst.setString(3, jComboG.getSelectedItem().toString());
            ptst.setString(4, dob.getText());
            ptst.setString(5, phn.getText());
            ptst.setString(6, email.getText());
            ptst.setString(7, ad.getText());
            
            
            ptst.executeUpdate();
            int contactid= db.getLastId("CONTACT");
			sql="insert into EMPLOYEE values(?,?,?,?)";

            ptst = db.con.prepareStatement(sql);
            ptst.setString(1, s.getText());
            ptst.setString(2, desig.getText());
            ptst.setString(3, ed.getText());
            ptst.setString(4,String.valueOf(contactid));
            
            ptst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Stored ");
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

